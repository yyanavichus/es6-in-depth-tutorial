// part 1:
var x = 3;
switch (x) {
    case 1:
        console.log("The number is one!");
    case 2:
        console.log("The number is two!");
    case 3:
        console.log("The number is three!");
}

// part 2:
var x = 1;
switch (x) {
    case 1:
        console.log("The number is one!");
        break;
    case 2:
        console.log("The number is two!");
        break;
    case 3:
        console.log("The number is three!");
        break;
}

// part 3:
var x = 13;
switch (x) {
    case 1:
        console.log("The number is one!");
        break;
    case 2:
        console.log("The number is two!");
        break;
    case 3:
        console.log("The number is three!");
        break;
    default:
        console.log("The number is " + x);
        break;
}
