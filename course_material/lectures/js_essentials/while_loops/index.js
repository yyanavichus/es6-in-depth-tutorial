// // all parts:
var names = ["Frodo", "Sam", "Merry", "Pippin"];
for (var i = 0; i < names.length; i++) {
    console.log(names[i]);
}

// part 1:
var i = 0;
while (i < names.length) {
    console.log(names[i]);
    i++;
}

// part 2:
while (i < names.length) {
    console.log(names[i]);
    // i++; // infinite loop!
}

// part 3:
var i = 0;
do {
    console.log(names[i]);
    i++;
} while (i < names.length);

// part 3:
var i = 0;
do {
    console.log(names[i]); // still executed once
    i++;
} while (i < 0);

// part 4:
var i = 0;
while (i < 0) {
    console.log(names[i]); // never reached
    i++;
}
